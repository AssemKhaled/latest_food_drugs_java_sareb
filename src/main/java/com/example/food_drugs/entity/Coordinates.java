package com.example.food_drugs.entity;

public class Coordinates {

	Double x;
	Double y;
	public Double getX() {
		return x;
	}
	public void setX(Double x) {
		this.x = x;
	}
	public Double getY() {
		return y;
	}
	public void setY(Double y) {
		this.y = y;
	}
	public Coordinates(Double x, Double y) {
		super();
		this.x = x;
		this.y = y;
	}
	public Coordinates() {

	}
	
}
